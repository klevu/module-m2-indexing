    public function testGet_OnlyReturnsImmutableAttributes(): void
    {
        $jsApiKey = 'klevu-js-key';
        $restAuthKey = 'klevu-rest-key';

        $this->createStore();
        $storeFixture = $this->storeFixturesPool->get('test_store');
